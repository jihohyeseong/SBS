package com.example.bookmarket.customer;

public class MyItem {
    public int no;
    public String id;
    public String title;
    public String content;
    public String date;

    public MyItem(int no, String id, String title, String content, String date) {
        this.no = no;
        this.id = id;
        this.title = title;
        this.content = content;
        this.date = date;
    }
}
